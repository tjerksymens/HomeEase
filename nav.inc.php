<script>
    const navBoxes = document.querySelectorAll('.nav_box');
    const checkedBoxes = document.querySelectorAll('.checked_nav_box');

    navBoxes.forEach(box => {
        box.addEventListener('click', () => {
            const link = box.querySelector('a').href;
            window.location.href = link;

            box.classList.add('hidden');
            const index = Array.from(navBoxes).indexOf(box);
            checkedBoxes[index].classList.remove('hidden');
        });
    });

    navBoxes.forEach(navBox => {
        const href = navBox.querySelector('a').getAttribute('href');
        if (window.location.href.includes(href)) {
            navBox.classList.add('hidden');
            const index = Array.from(navBoxes).indexOf(navBox);
            checkedBoxes[index].classList.remove('hidden');
        }
    });
</script>